#标准算数运算符
#包含加(+)、减(-)、乘(*)、除(/)、整除(//)
print(1+1)
print(1-1)
print(9*1)
print(8/2)
print(11//2)
#取余运算符 %
print(11%2)#取除法的余数
#幂运算符 **
print(2**3)

#特殊运算
print(9//4)
print(-9//4)#相当于取整数部分-2.25的整数部分为-3 一正一负整数公式向下取整

print(-9%4) #3  公式  余数=被除数-除数*商  -9-4*(-3)-->3
print(9%-4) #-3  9-(-4)*(-3)  9-12———>-3
