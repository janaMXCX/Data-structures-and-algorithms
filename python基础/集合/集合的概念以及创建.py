#集合是可变类型
#集合是没有value的字典，顺序不固定

"""创建方式"""
#可以直接用花括号进行创建{}
s={55,4,5,4,9,9,8,7}
print(s)
#集合中的元素不允许重复
#第二种创建方式使用set()进行创建
s1=set(range(6))
print(s1)
s2=set([1,2,3,5,4,5,5,6])#列表
print(s2,type(s2))

s3=set((1,2,3,5,4,8,65))#元组
print(s3)#集合中的元素是无序的

s4=set(('python'))#字符串
print(s4)

s5=set({1,5,6,5,8,4,5,4,3})
print(s5,type(s5))

s6={}#此处定义的是空字典
s7=set()#此处定义的时空集合
print(type(s6),type(s7))